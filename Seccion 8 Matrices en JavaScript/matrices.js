// Matrices en JS
// Una matriz es un arreglo de 2 dimensiones

// Declaracion de una matriz
let matriz = [[],[]];

// Modificar los valores
// Renglon 0
matriz[0][0] = 100;
matriz[0][1] = 200;
matriz[0][2] = 300;

// Renglon 1
matriz[1][0] = 400;
matriz[1][1] = 500;
matriz[1][2] = 600;

// Leer los valores
console.log(`Elemento [0][1] = ${matriz[0][1]}`);
console.log(`Elemento [1][2] = ${matriz[1][2]}`);