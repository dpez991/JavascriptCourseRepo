// Arreglos en JS
// Un arreglo nos permite almaecnar multiples valores en una sola variable, 
// en lugar de definir varias variables

// Declaracion del arreglo
let numerosArreglo = [13, 21, 0, 0, 62];


// Leer los valores
console.log(numerosArreglo.length);
for(let i = 0; i < numerosArreglo.length; i++){
    console.log(`Arreglo[${i}] = ${numerosArreglo[i]}`);
};