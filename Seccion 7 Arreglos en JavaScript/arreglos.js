// Arreglos en JS
// Un arreglo nos permite almaecnar multiples valores en una sola variable, 
// en lugar de definir varias variables

// Declaracion del arreglo
let numerosArreglo = [];

// Modificar los valores
numerosArreglo[0] = 13;
numerosArreglo[1] = 21;
numerosArreglo[4] = "hola";

// Leer los valores
console.log(`Elemento 1 - [0]: ${numerosArreglo[0]}`);
console.log(`Elemento 3 - [2]: ${numerosArreglo[3]}`);
console.log(`Elemento 5 - [4]: ${numerosArreglo[4]}`);